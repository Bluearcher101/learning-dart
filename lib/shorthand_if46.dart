void main(){
   //shorthand if is a way to use the if function in a way that is brief
   int a = 32;

   a > 30 ? print("a > 30 ") : print("a < 30");
   //after putting the ? you put the function that will activate if the condition is true
   // the function you put after : is the one that will activate if the condition is false

   //there more additions to the if condition you can put like ||(or) &&(and) for example
   String name = "rami";
   name = "sabbagh";

   name == "rami" || name == "sabbagh" ? print("my name is rami sabbagh") : print("that is not my name");

}